// Arithmetic operators

// 1. Addition( + )
let sum = 5 + 3;
console.log("Addition:", sum);

// 2. Subtraction( - )
let difference = 8 - 3;
console.log("Subtraction:", difference);

// 3. Multiplication( * )
let product = 4 * 6;
console.log("Multiplication:", product);

// 4. Division( / )
let quotient = 10 / 2;
console.log("Division:", quotient);

// 5. Remainder( % )
let remainder = 15 % 4;
console.log("Remainder:", remainder);

// 6. Unary plus( + )
let numString = "5";
let convertedNum = +numString;
console.log("Unary plus:", convertedNum);

// 7. Unary negation( - )
let negationResult = -convertedNum;
console.log("Unary negation:", negationResult);

// 8. Exponentiation( ** )
let base = 2;
let exponent = 3;
let resultExponentiation = base ** exponent;
console.log("Exponentiation:", resultExponentiation);

// 9. Increment( ++ )
let x = 5;
let preIncrement = ++x;
console.log("Pre-increment:", preIncrement);

let y = 8;
let postIncrement = y++;
console.log("Post-increment:", postIncrement);

// 10. Decrement( -- )
let a = 10;
let preDecrement = --a;
console.log("Pre-decrement:", preDecrement);

let b = 7;
let postDecrement = b--;
console.log("Post-decrement:", postDecrement);
